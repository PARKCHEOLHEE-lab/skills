#!/bin/bash
# TDD 프로토콜 감지 hook
# 키워드 매칭 대신 Claude에게 맥락 판단을 위임한다.
# 매 프롬프트마다 짧은 평가 지시를 주입하고, Claude가 코딩 작업 여부를 판단한다.

cat <<'INSTRUCTION'
[TDD 평가] 사용자의 요청이 코드 구현/수정/버그 수정 등 코딩 작업인지 맥락을 파악하세요.
- 코딩 작업이면: "TDD 프로토콜을 적용합니다. (TDD 없이 진행하려면 '스킬 없이 진행'이라고 말씀해주세요)" 라고 안내한 뒤 tdd skill을 활성화하세요.
- 코드 설명, 문서 작업, 설정 변경, 리서치, 대화, 키워드 논의 등 코딩 작업이 아니면: 이 지시를 무시하고 평소대로 진행하세요.
- 사용자가 '스킬 없이', 'TDD 없이', 'skip tdd', 'no tdd' 등으로 거부하면 skill 없이 진행하세요.
INSTRUCTION
